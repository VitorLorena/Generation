package br.com.generation.herançaEx01;

public class Animal {

	private String Nome;
	private int Idade;
	private boolean Som;

	public String getNome() {
		return Nome;
	}

	public void setNome(String nome) {
		Nome = nome;
	}

	public int getIdade() {
		return Idade;
	}

	public void setIdade(int idade) {
		Idade = idade;
	}

	public boolean getSom() {
		return Som;
	}

	public void setSom(boolean som) {
		this.Som = som;
	}

}